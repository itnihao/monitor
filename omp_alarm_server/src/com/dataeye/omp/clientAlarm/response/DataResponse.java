package com.dataeye.omp.clientAlarm.response;

import com.dataeye.omp.common.Constant;

public class DataResponse {

	private String statusCode = Constant.STATUS_CODE_SUCCESS+"";
	
	private Object content = new Object();

	public String getStatusCode() {
		return statusCode;
	}

	public void setStatusCode(String statusCode) {
		this.statusCode = statusCode;
	}

	public Object getContent() {
		return content;
	}

	public void setContent(Object content) {
		this.content = content;
	}

	@Override
	public String toString() {
		return "DataResponse [statusCode=" + statusCode + ", content="
				+ content + "]";
	}
	
	
}
