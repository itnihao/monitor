package com.dataeye.omp.common;

public class CustomAlarmRule {

	private long id;
	
	private long customizeID;
	
	private int alarmInterval;
	
	private int maxFrequency;
	
	private int restoreType;
	
	private int restoreInterval;
	
	private String alarmType;

	/**
	 * @return the id
	 */
	public long getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(long id) {
		this.id = id;
	}

	/**
	 * @return the customizeID
	 */
	public long getCustomizeID() {
		return customizeID;
	}

	/**
	 * @param customizeID the customizeID to set
	 */
	public void setCustomizeID(long customizeID) {
		this.customizeID = customizeID;
	}

	/**
	 * @return the alarmInterval
	 */
	public int getAlarmInterval() {
		return alarmInterval;
	}

	/**
	 * @param alarmInterval the alarmInterval to set
	 */
	public void setAlarmInterval(int alarmInterval) {
		this.alarmInterval = alarmInterval;
	}

	/**
	 * @return the maxFrequency
	 */
	public int getMaxFrequency() {
		return maxFrequency;
	}

	/**
	 * @param maxFrequency the maxFrequency to set
	 */
	public void setMaxFrequency(int maxFrequency) {
		this.maxFrequency = maxFrequency;
	}

	/**
	 * @return the restoreType
	 */
	public int getRestoreType() {
		return restoreType;
	}

	/**
	 * @param restoreType the restoreType to set
	 */
	public void setRestoreType(int restoreType) {
		this.restoreType = restoreType;
	}

	/**
	 * @return the restoreInterval
	 */
	public int getRestoreInterval() {
		return restoreInterval;
	}

	/**
	 * @param restoreInterval the restoreInterval to set
	 */
	public void setRestoreInterval(int restoreInterval) {
		this.restoreInterval = restoreInterval;
	}

	/**
	 * @return the alarmType
	 */
	public String getAlarmType() {
		return alarmType;
	}

	/**
	 * @param alarmType the alarmType to set
	 */
	public void setAlarmType(String alarmType) {
		this.alarmType = alarmType;
	}
	
	
}
