package com.dataeye.omp.module.cmdb.device;

import java.util.List;

/**
 * 机房信息
 * @auther wendy
 * @since 2015/12/27 13:45
 */
public class Idc {
    private int id;

    private String name;

    private String contacts;

    private String address;

    private List<Cabinat> cabinatList;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getContacts() {
        return contacts;
    }

    public void setContacts(String contacts) {
        this.contacts = contacts;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public List<Cabinat> getCabinatList() {
        return cabinatList;
    }

    public void setCabinatList(List<Cabinat> cabinatList) {
        this.cabinatList = cabinatList;
    }

}
