package com.dataeye.omp.module.alarm;

import com.google.gson.annotations.Expose;


/**
 * 告警规则
 * @auther wendy
 * @since 2016/2/19 14:37
 */
public class AlarmRule {

    @Expose
    /**告警规则ID*/
    private int id;

    @Expose
    /**特性ID*/
    private int featureID;

    @Expose
    /**特性名称*/
    private String featureName;

    @Expose
    /**监控对象*/
    private String object;

    /**告警对象 json*/
    private String alarmObjectString;

    @Expose
    /**告警对象*/
    private AlarmObject alarmObject;

    /**告警规则 json*/
    private String alarmRuleString;

    @Expose
    /**告警规则*/
    private Rule rule;

    @Expose
    /**屏蔽时间 开始*/
    private String shieldStart;

    @Expose
    /**屏蔽时间  结束*/
    private String shieldEnd;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getFeatureID() {
        return featureID;
    }

    public void setFeatureID(int featureID) {
        this.featureID = featureID;
    }

    public String getObject() {
        return object;
    }

    public void setObject(String object) {
        this.object = object;
    }

    public String getAlarmObjectString() {
        return alarmObjectString;
    }

    public void setAlarmObjectString(String alarmObjectString) {
        this.alarmObjectString = alarmObjectString;
    }

    public AlarmObject getAlarmObject() {
        return alarmObject;
    }

    public void setAlarmObject(AlarmObject alarmObject) {
        this.alarmObject = alarmObject;
    }

    public String getAlarmRuleString() {
        return alarmRuleString;
    }

    public void setAlarmRuleString(String alarmRuleString) {
        this.alarmRuleString = alarmRuleString;
    }

    public Rule getRule() {
        return rule;
    }

    public void setRule(Rule rule) {
        this.rule = rule;
    }

    public String getShieldStart() {
        return shieldStart;
    }

    public void setShieldStart(String shieldStart) {
        this.shieldStart = shieldStart;
    }

    public String getShieldEnd() {
        return shieldEnd;
    }

    public void setShieldEnd(String shieldEnd) {
        this.shieldEnd = shieldEnd;
    }

    public String getFeatureName() {
        return featureName;
    }

    public void setFeatureName(String featureName) {
        this.featureName = featureName;
    }
}
