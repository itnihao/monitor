package com.dataeye.omp.report.bean;

import java.util.List;

/**
 * @author wendy
 * @since 2016/7/29
 */
public class ProcessInfo{

    /**进程名称*/
    private String name;

    /**ip*/
    private String ip;

    /**端口*/
    private Integer port;

    /**进程状态*/
    private Integer processStatus;

    /**端口状态*/
    private Integer portStatus;

    /**时间戳*/
    private String time;

    private List<ProcessInfo> process_list;

    public List<ProcessInfo> getProcess_list() {
        return process_list;
    }

    public void setProcess_list(List<ProcessInfo> process_list) {
        this.process_list = process_list;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public int getProcessStatus() {
        return processStatus;
    }

    public void setProcessStatus(int processStatus) {
        this.processStatus = processStatus;
    }

    public int getPortStatus() {
        return portStatus;
    }

    public void setPortStatus(int portStatus) {
        this.portStatus = portStatus;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

}


